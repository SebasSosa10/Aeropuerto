    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        String Usuario = txtUsuario.getText();
        String Contrasenia = txtContrasenia.getText();
        AdministradorAerolinea admin = controladorInicio.buscarAdmin(Usuario, Contrasenia);
        Persona person = controladorInicio.buscarEmpleado(Usuario, Contrasenia);
        Persona viajero = controladorInicio.buscarViajero(Usuario, Contrasenia);
        if (Usuario.equals("admin") && Contrasenia.equals("admin")) {
            VentanaAdministradorGeneral adminG = new VentanaAdministradorGeneral();
            adminG.setVisible(true);
            adminG.setLocationRelativeTo(this);
            this.dispose();
        } else if (admin != null) {
            Aerolinea aero = controladorInicio.inicioAerolinea(Usuario, Contrasenia);
            VentanaAdministradorAerolinea adminA = new VentanaAdministradorAerolinea(aero);
            adminA.setVisible(true);
            adminA.setLocationRelativeTo(this);
            dispose();
        } else if (person != null && person instanceof EmpleadoLogistica) {
            Aerolinea aeroli = controladorInicio.inicioAerolineaEmpleado(Usuario, Contrasenia);
            VentanaEmpleado emple = new VentanaEmpleado(aeroli);
            emple.setVisible(true);
            emple.setLocationRelativeTo(this);
            dispose();
        } else if (viajero != null && viajero instanceof Viajero) {
            VentanaViajeroMenu viaje = new VentanaViajeroMenu((Viajero) viajero);
            viaje.setVisible(true);
            viaje.setLocationRelativeTo(this);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "No Se Encontro El Usuario");
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

