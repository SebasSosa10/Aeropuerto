    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String usuario = txtUsuarioGes.getText();
        String contrasenia = txtContraseniaGes.getText();
        String nombre = txtNombreGes.getText();
        String apellido = txtApellidoGes.getText();
        String genero = cbGenero.getSelectedItem().toString();
        String numCelular = txtNumCelularGes.getText();
        String numDocumen = txtNumDocumentoGes.getText();
        Persona gestor = new GestorMantenimineto(nombre, apellido, genero, numCelular, numDocumen, usuario, contrasenia);
        boolean respuesta = controladorGestorMantenimiento.guardarGestor(gestor);
        if (respuesta) {
            JOptionPane.showMessageDialog(null, "Se guardo el Gestor de mantenimiento de la Aerolinea");
            limpiarGest();
        } else {
            JOptionPane.showMessageDialog(null, " No se guardo el Gestor de mantenimiento de la Aerolinea");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String codigo = txtNumDocumentoGes.getText();
        Persona gestor = controladorGestorMantenimiento.buscarGestor(codigo);
        if (gestor != null) {
            txtUsuarioGes.setText(gestor.getUsuario());
            txtContraseniaGes.setText(gestor.getContrasenia());
            txtNombreGes.setText(gestor.getNombre());
            txtApellidoGes.setText(gestor.getApellido());
            cbGenero.setSelectedItem(gestor.getGenero());
            txtNumCelularGes.setText(gestor.getNumCelular());
        } else {
            JOptionPane.showMessageDialog(null, "No Existe El Gestor de mantenimiento De Logistica");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

