    private void txtNombreGesKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreGesKeyTyped
        char letras = evt.getKeyChar();
        if ((letras < 'a' || letras > 'z') && (letras < 'A' || letras > 'Z'))
            evt.consume();
    }//GEN-LAST:event_txtNombreGesKeyTyped

