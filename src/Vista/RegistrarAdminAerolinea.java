    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String codAero = txtCodigoAerolinea.getText();
        Aerolinea respuesta =controladorAerolinea.buscarAereoLinea(codAero);
        if (respuesta != null) {
            txtUsuario.setText(respuesta.getAdministradorAerolinea().getUsuario());
            txtContrasenia.setText(respuesta.getAdministradorAerolinea().getContrasenia());
            txtNombre.setText(respuesta.getAdministradorAerolinea().getNombre());
            txtApellido.setText(respuesta.getAdministradorAerolinea().getApellido());
            cbGenero.setSelectedItem(respuesta.getAdministradorAerolinea().getGenero());
            txtNumCelular.setText(respuesta.getAdministradorAerolinea().getNumCelular());
            txtIdiomas.setText(respuesta.getAdministradorAerolinea().getIdioma());
            txtNumDocumento.setText(respuesta.getAdministradorAerolinea().getNumDocumento());
            txtNomAero.setText(respuesta.getNombreAerolinea());
            actualizarTablaAdmin();
            actualizarTablaAerolinea();
        } else {
            JOptionPane.showMessageDialog(null, " No Se Encontro la Aerolinea");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

