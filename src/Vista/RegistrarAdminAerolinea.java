     private void actualizarTablaAdmin() {
        DefaultTableModel modelo = new DefaultTableModel(new String[]{"NOMBRE", "APELLIDO", "GENERO", "N° DOCUMENTO",
            "AEROLINEA"},
                controladorAerolinea.getListaAerolineas().size());
        tblAdmin.setModel(modelo);
        TableModel modeloDatos = tblAdmin.getModel();
        for (int i = 0; i < controladorAerolinea.getListaAerolineas().size(); i++) {
            Aerolinea aux = controladorAerolinea.getListaAerolineas().get(i);
            modeloDatos.setValueAt(aux.getAdministradorAerolinea().getNombre(), i, 0);
            modeloDatos.setValueAt(aux.getAdministradorAerolinea().getApellido(), i, 1);
            modeloDatos.setValueAt(aux.getAdministradorAerolinea().getGenero(), i, 2);
            modeloDatos.setValueAt(aux.getAdministradorAerolinea().getNumDocumento(), i, 3);
            modeloDatos.setValueAt(aux.getNombreAerolinea(), i, 4);
        }
    }
     
     private void actualizarTablaAerolinea() {
        DefaultTableModel modelo = new DefaultTableModel(new String[]{"NOMBRE AEROLINEA", "CODIGO DE AEROLINEA"},
                controladorAerolinea.getListaAerolineas().size());
        tblAerolinea.setModel(modelo);
        TableModel modeloDatos = tblAerolinea.getModel();
        for (int i = 0; i < controladorAerolinea.getListaAerolineas().size(); i++) {
            Aerolinea aux = controladorAerolinea.getListaAerolineas().get(i);
            modeloDatos.setValueAt(aux.getNombreAerolinea(), i, 0);
            modeloDatos.setValueAt(aux.getCodigoAerolinea(), i, 1);
        }
    }
     
