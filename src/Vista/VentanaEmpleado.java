    private void actualizarSolicitud() {
        DefaultTableModel modelo = new DefaultTableModel(new String[]{"ID", "FECHA", "AVION", "OBSERVACIÓN"},
                aeroline.getListaSolicitud().size());
        tblSolicitud.setModel(modelo);
        TableModel modeloDatos = tblSolicitud.getModel();
        for (int i = 0; i < aeroline.getListaSolicitud().size(); i++) {
            SolicitudMantenimiento aux = aeroline.getListaSolicitud().get(i);
            modeloDatos.setValueAt(aux.getId(), i, 0);
            modeloDatos.setValueAt(aux.getFecha().toString(), i, 1);
            modeloDatos.setValueAt(aux.getAvion(), i, 2);
            modeloDatos.setValueAt(aux.getObservacion(), i, 3);
        }
    }


    private void txtRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtRegistrarActionPerformed
        try {
            String origen = cbOrigen.getSelectedItem().toString();
            String destino = cbDestino.getSelectedItem().toString();
            int horasalida = Integer.parseInt(cbHoraSalida.getSelectedItem().toString());
            int minutoSalida = Integer.parseInt(cbMinutoSalida.getSelectedItem().toString());
            int horatiempoTrayectoria = Integer.parseInt(cbHoraTrayectoria.getSelectedItem().toString());
            int minutoTrayectoria = Integer.parseInt(cbMinutoTrayectoria.getSelectedItem().toString());
            String codigovuelo = txtCodigoVuelo.getText();
            Avion codigoAvion = (Avion) cbAvion.getSelectedItem();
            CapitanVuelo capitan = (CapitanVuelo) cbCapitan.getSelectedItem();
            Date fechaAux = jdcFechaVuelo.getDate();
            LocalDate fecha = fechaAux.toInstant().atZone(java.time.ZoneId.systemDefault()).toLocalDate();
            LocalTime fechaHora = LocalTime.of(horasalida, minutoSalida);
            LocalDateTime fechaSalida = LocalDateTime.of(fecha, fechaHora);
            LocalDateTime fechaLlegada = fechaSalida.plusHours(horatiempoTrayectoria);
            fechaLlegada.plusMinutes(minutoTrayectoria);
            Vuelo vuel = new Vuelo(origen, destino, fechaSalida, fechaLlegada, codigovuelo, codigoAvion, capitan);
            Vuelo vuelo2 = controladorVuelo.validacionOrigen(aeroline, vuel);
            if (vuelo2 != null) {
                int E = JOptionPane.showConfirmDialog(null, "El vuelo ya se encuentra en otro Ciudad quiere cambiar el punto de origen");
                if (E == 0) {
                    Vuelo vu1 = new Vuelo(vuelo2.getDestino(), destino, fechaSalida, fechaLlegada, codigovuelo, codigoAvion, capitan);
                    this.aeroline = controladorVuelo.guardarVuelo(aeroline, vu1);
                    actualizarTabla();
                    limpiarVuelo();
                    JOptionPane.showMessageDialog(null, "Se guardo El Vuelo");
                }
            } else {
                this.aeroline=controladorVuelo.guardarVuelo(aeroline, vuel);
                JOptionPane.showMessageDialog(null, "Se guardo El Vuelo");
                actualizarTabla();
                limpiarVuelo();
            }
        } catch (mismoOrigenException | vueloDuplicadoException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }


    }//GEN-LAST:event_txtRegistrarActionPerformed

