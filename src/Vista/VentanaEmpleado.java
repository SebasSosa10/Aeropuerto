    private void actualizarTabla() {
        DefaultTableModel modelo = new DefaultTableModel(new String[]{"ORIGEN", "DESTINO", "FECHA", "HORA SALIDA",
            "TIEMPO TRAYECTORIA", "AVION", "CODIGO VUELO"},
                aeroline.getListaVuelos().size());
        tblTabla.setModel(modelo);
        TableModel modeloDatos = tblTabla.getModel();
        for (int i = 0; i < aeroline.getListaVuelos().size(); i++) {
            Vuelo aux = aeroline.getListaVuelos().get(i);
            SimpleDateFormat standar1 = new SimpleDateFormat("dd/MM/yyyy");
            modeloDatos.setValueAt(aux.getOrigen(), i, 0);
            modeloDatos.setValueAt(aux.getDestino(), i, 1);
            modeloDatos.setValueAt(standar1.format(java.sql.Date.valueOf(aux.getFechaLlegada().toLocalDate())), i, 2);
            modeloDatos.setValueAt(((aux.getFechaSalida().toLocalTime().toString())), i, 3);
            modeloDatos.setValueAt(aux.getFechaLlegada().toLocalTime().toString(), i, 4);
            modeloDatos.setValueAt(aux.getAvion().getModelo(), i, 5);
            modeloDatos.setValueAt(aux.getCodigoVuelo(), i, 6);
        }
    }

